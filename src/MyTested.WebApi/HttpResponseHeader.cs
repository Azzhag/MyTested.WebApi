namespace MyTested.WebApi
{
    /// <summary>
    /// Contains common HTTP response header names.
    /// </summary>
    public class HttpResponseHeader
    {
        /// <summary>
        /// Represents AcceptRanges content header name.
        /// </summary>
        public const string AcceptRanges = "AcceptRanges";

        /// <summary>
        /// Represents Age content header name.
        /// </summary>
        public const string Age = "Age";

        /// <summary>
        /// Represents CacheControl content header name.
        /// </summary>
        public const string CacheControl = "CacheControl";

        /// <summary>
        /// Represents Connection content header name.
        /// </summary>
        public const string Connection = "Connection";

        /// <summary>
        /// Represents ConnectionClose content header name.
        /// </summary>
        public const string ConnectionClose = "ConnectionClose";

        /// <summary>
        /// Represents Date content header name.
        /// </summary>
        public const string Date = "Date";

        /// <summary>
        /// Represents Location content header name.
        /// </summary>
        public const string Location = "Location";

        /// <summary>
        /// Represents Pragma content header name.
        /// </summary>
        public const string Pragma = "Pragma";

        /// <summary>
        /// Represents ProxyAuthenticate content header name.
        /// </summary>
        public const string ProxyAuthenticate = "ProxyAuthenticate";

        /// <summary>
        /// Represents RetryAfter content header name.
        /// </summary>
        public const string RetryAfter = "RetryAfter";

        /// <summary>
        /// Represents Server content header name.
        /// </summary>
        public const string Server = "Server";

        /// <summary>
        /// Represents Trailer content header name.
        /// </summary>
        public const string Trailer = "Trailer";

        /// <summary>
        /// Represents TransferEncoding content header name.
        /// </summary>
        public const string TransferEncoding = "TransferEncoding";

        /// <summary>
        /// Represents Upgrade content header name.
        /// </summary>
        public const string Upgrade = "Upgrade";

        /// <summary>
        /// Represents Vary content header name.
        /// </summary>
        public const string Vary = "Vary";

        /// <summary>
        /// Represents Via content header name.
        /// </summary>
        public const string Via = "Via";

        /// <summary>
        /// Represents Warning content header name.
        /// </summary>
        public const string Warning = "Warning";

        /// <summary>
        /// Represents WwwAuthenticate content header name.
        /// </summary>
        public const string WwwAuthenticate = "WwwAuthenticate";
    }
}
